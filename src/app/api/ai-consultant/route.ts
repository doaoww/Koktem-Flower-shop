export async function POST(req: NextRequest) {
  try {
    const { messages } = await req.json();

    const userMessages = messages.filter(
      (m: { role: string; content: string }) => m.role === "user"
    );

    if (userMessages.length === 0) {
      return NextResponse.json({ message: "Напишите ваш вопрос!" });
    }

    const model = genAI.getGenerativeModel({
      model: "gemini-2.5-flash",
      systemInstruction: SYSTEM_PROMPT,
    });

    // Строим историю для контекста
    const history = messages
      .slice(0, -1)
      .filter((m: { role: string }) => m.role === "user" || m.role === "assistant")
      .map((m: { role: string; content: string }) => ({
        role: m.role === "assistant" ? "model" : "user",
        parts: [{ text: m.content }],
      }))
      // Убираем если первое сообщение от model
      .filter((_: unknown, i: number, arr: unknown[]) => !(i === 0 && (arr[0] as { role: string }).role === "model"));

    const chat = model.startChat({ history });

    const lastMessage = messages[messages.length - 1].content;
    const result = await chat.sendMessage(lastMessage);
    const text = result.response.text();

    return NextResponse.json({ message: text });
  } catch (e: unknown) {
    console.error("Gemini Error:", e);
    return NextResponse.json(
      { error: e instanceof Error ? e.message : "Ошибка AI консультанта" },
      { status: 500 }
    );
  }
}